    <p>Een kettingzaag is een draagbare machine om mee te zagen. De belangrijkste functie is hout zagen.
Er zijn verschillende typen aandrijving voor kettingzagen: pneumatisch, hydraulisch, elektrisch en door middel van een verbrandingsmotor. De elektrische kettingzaag en de motorkettingzaag zijn het meest gangbaar.
Een kettingzaag bestaat uit drie delen:
1.	Motor: de benzinemotor moet gestart worden met een startkoord (als bij een motormaaier). De motor drijft een kettingrondsel aan, dat zorgt dat de ketting gaat ronddraaien.
2.	Zaagblad: het zaagblad of "zwaard" zit aan de motorbehuizing vastgemaakt. Het zaagblad heeft over de hele lengte een groef.
3.	Zaagketting: een schakelketting met zaagtanden, ook wel beitels genoemd. Een zaagketting bestaat uit drie onderdelen: de zaagtand, de dieptesteller en de aandrijfschakel.
