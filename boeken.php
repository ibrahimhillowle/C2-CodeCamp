        <h2>Wurgtechnieken – Ward Ruislink</h2>
Ward Ruyslinck ( Raymond De Belser) was een Vlaams schrijver. Hij was een veelgelezen auteur, ook onder humaniorastudenten. Ruyslinck werd door zijn bewonderaars genoemd als een Belgische kanshebber op de Nobelprijs voor de Literatuur. Ruyslinck verweeft in zijn hele werk maatschappijkritiek en hoop op een betere tijd doordat hij zijn puberteit doormaakte tijdens de Tweede Wereldoorlog. Hij stelt zich vragen bij de grote onderdrukkende systemen als het kapitalisme, de (katholieke) godsdienst en de staat met zijn militaire apparaat. Toch wordt die kritiek omzichtig, soms sentimenteel, soms pathetisch geuit. Daardoor komt zijn stijl in de 21e eeuw wat bombastisch over, terwijl zijn vragen actueel blijven. Aangezien Ruyslinck zich niet opwierp als de grote hervormer of beeldenstormer en zijn vertellingen het kleinburgerlijke zwoegende bestaan van de meerderheid beschrijven, stuitte hij niet op afwijzing van het Belgische establishment.
Werk van hem is vertaald in circa twaalf talen (vooral Duits en Engels). De stille zomer (1962) en Wierook en tranen (1958) zijn verfilmd en onder andere De ontaarde slapers (1957) en Het reservaat (1964) zijn voor toneel bewerkt.</p>
