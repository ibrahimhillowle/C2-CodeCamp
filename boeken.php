      <h2>Moonraker (novel)</h2>
      <p>Moonraker is the third novel by the British author Ian Fleming to feature his fictional British Secret Service
        agent James Bond. It was published by Jonathan Cape on 5 April 1955 and featured a cover design conceived by
        Fleming. The plot is derived from a Fleming screenplay that was too short for a full novel so he added the
        passage of the bridge game between Bond and the industrialist Hugo Drax. In the latter half of the novel, Bond
        is seconded to Drax's staff as the businessman builds the Moonraker, a prototype missile designed to defend
        England. Unknown to Bond, Drax is German, an ex-Nazi now working for the Soviets; his plan is to build the
        rocket, arm it with a nuclear warhead, and fire it at London. Uniquely for a Bond novel, Moonraker is set
        entirely in Britain, which raised comments from some readers, complaining about the lack of exotic locations.
        <br>
        <br>
        Moonraker, like Fleming's previous novels, was well received by critics. It plays on several 1950s fears,
        including attack by rockets (following the V-2 strikes of the Second World War), nuclear annihilation, Soviet
        communism, the re-emergence of Nazism and the "threat from within" posed by both ideologies. Fleming examines
        Englishness, and the novel shows the virtues and strength of England. Adaptations include a broadcast on South
        African radio in 1956 starring Bob Holness and a 1958 Daily Express comic strip. The novel's name was used in
        1979 for the eleventh official film in the Eon Productions Bond series and the fourth to star Roger Moore as
        Bond; the plot was significantly changed from the novel to include excursions into space.</p>
